package Abstraction;

public class AbstractionDocs {
/*
 * 
 * =======Abstraction ======
 * 	 # process of hiding implementation details in the program is known as abstraction.
 * 	#  To aechive Abstraction :
 *   		1> Abstract Class:
 *    		# class which contains abstract (unimplemented)and non-abstract method(implemented) 
 *            is known as abstract class.
 *            # abstract Method:
 *            * method which does not have implementation details. or does not have body.
 *            * E.g:
 *             abstract void print(){
 *                ....
 *              }
 *             * Every abstract class must have at least one child class and child calss must override 
 *               abstract methods.
 *             * We can not create object of abstract class.
 *             * level of abstraction (0-100%).
 *             
 *             Syntax:
 *             abstract class class-name{
 *             
 *             //abstract methods
 *             
 *             //non-abstract methods.
 *             
 *             }
 *    
 *    		2> Interface:
 *    
 *    # same as class which contains public abstract method and public static final variables only:
 *    # every interface must have at least one implementation class and impl. class must override 
 *      abstract methods.
 *    # we can't create object of interface.
 *    # 100% abstraction.
 *    Syntax:
 *    interface interface-name{
 *    //public static final variable
 *    
 *    // public abstract methods.
 *       }
 *         
 */
}
