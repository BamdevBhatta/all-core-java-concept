package ClassObjectConcept;

public class Rectangle {
int length;
int breadth;
  			void area() {
  				int a = length*breadth;
  				System.out.println("Area of Rectangle is: "+a);
  			}
  			void perimeter() {
  				int b = 2*(length+breadth);
  				System.out.println("Perimeter of rectangle is:"+b);
  			}
	}
